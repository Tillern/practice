class User {
  final String name;
  final String email;
  final String picture;
  final String gender;
  final String location;
  final String city;
  final String dob;
  final String registeredDate;

  User({
    required this.name,
    required this.email,
    required this.picture,
    required this.gender,
    required this.location,
    required this.city,
    required this.dob,
    required this.registeredDate,
  });
}